import cn from "classnames";

export function setPageTitle(str) {
  const cut = str.trim().substr(0, 119);
  document.title = `JS Blog. ${cut}`;
}

export function fieldErrorTip(error){
  return error && (
    <span className={cn('note_field', 'error', 'show')}>
      { error?.message }
    </span>
  )
}

